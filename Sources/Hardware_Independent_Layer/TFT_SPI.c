void spi1_init(void) //systemclock 48MHZ
{  
	  SIM_COPC = 0x00U;                                                   
	  SIM_SCGC5 |= SIM_SCGC5_PORTA_MASK;   /* Enable clock gate for ports to enable pin routing */
	  SIM_CLKDIV1 = SIM_CLKDIV1_OUTDIV4(0x01); /* Update system prescalers */
	  /* SIM_SOPT2: PLLFLLSEL=0 */
	  SIM_SOPT2 &= ~(SIM_SOPT2_PLLFLLSEL_MASK); /* Select FLL as a clock source for various peripherals */
	  /* SIM_SOPT1: OSC32KSEL=3 */
	  SIM_SOPT1 |= SIM_SOPT1_OSC32KSEL(0x03); /* LPO 1kHz oscillator drives 32 kHz clock for various peripherals */
	  /* SIM_SOPT2: TPMSRC=1 */
	  SIM_SOPT2 |= SIM_SOPT2_TPMSRC(0x01);//(uint32_t)((SIM_SOPT2 & (uint32_t)~(uint32_t)(SIM_SOPT2_TPMSRC(0x02))) | (uint32_t)(SIM_SOPT2_TPMSRC(0x01)));                      /* Set the TPM clock */
	  /* Switch to FEI Mode */
	  /* MCG_C1: CLKS=0,FRDIV=0,IREFS=1,IRCLKEN=1,IREFSTEN=0 */
	  MCG_C1 = MCG_C1_IREFS_MASK | MCG_C1_IRCLKEN_MASK;                                                   
	  /* MCG_C2: LOCRE0=0,RANGE0=0,HGO0=0,EREFS0=0,LP=0,IRCS=0 */
	  MCG_C2 = 0x00;                                                   
	  /* MCG_C4: DMX32=1,DRST_DRS=2 */
	 /* MCG_C4 = (uint8_t)((MCG_C4 & (uint8_t)~(uint8_t)(
	            MCG_C4_DRST_DRS(0x02)
	           )) | (uint8_t)(
	            MCG_C4_DMX32_MASK |
	            MCG_C4_DRST_DRS(0x01)
	           )); */ //For a 48 MHz clk in real 51Mhz
	  MCG_C4 = MCG_C4_DMX32_MASK | MCG_C4_DRST_DRS(0x2); //96Mhz
	  /* OSC0_CR: ERCLKEN=1,EREFSTEN=0,SC2P=0,SC4P=0,SC8P=0,SC16P=0 */
	  OSC0_CR = OSC_CR_ERCLKEN_MASK;                                                   
	  /* MCG_C5: PLLCLKEN0=0,PLLSTEN0=0,PRDIV0=0 */
	  MCG_C5 = 0x00;                                                   
	  /* MCG_C6: LOLIE0=0,PLLS=0,CME0=0,VDIV0=0 */
	  MCG_C6 = 0x00;                                                   
	  while((MCG_S & MCG_S_IREFST_MASK) == 0x00); /* Check that the source of the FLL reference clock is the internal reference clock. */
	  while((MCG_S & 0x0CU) != 0x00);    /* Wait until output of the FLL is selected */
	  
	  
 SIM_CLKDIV1 |= SIM_CLKDIV1_OUTDIV1(0x00);
 SIM_CLKDIV1 |= SIM_CLKDIV1_OUTDIV4(0x00); 
 SIM_SCGC5 |= SIM_SCGC5_PORTD_MASK;      //Turn on clock to D module  
 SIM_SCGC4 |= SIM_SCGC4_SPI1_MASK;       //Enable SPI1 clock  
  
  PORTD_PCR2 = PORT_PCR_MUX(0x1);	 //Set PTD2 to mux 1 [SPI1_RS//SPI1_A0] Data/command
  PORTD_PCR3 = PORT_PCR_MUX(0x1);	 //Set PTD3 to mux 1 [SPI1_Reset//Reset] 
  PORTD_PCR4 = PORT_PCR_MUX(0x2);    //Set PTD4 to mux 2 [SPI1_PCS0]  
  PORTD_PCR5 = PORT_PCR_MUX(0x2);    //Set PTD5 to mux 2 [SPI1_SCK]  
  PORTD_PCR6 = PORT_PCR_MUX(0x2);    //Set PTD6 to mux 2 [SPI1_MOSI]  
  PORTD_PCR7 = PORT_PCR_MUX(0x2);    //Set PTD7 to mux 2 [SPI1_MISO]  
  
  GPIOD_PDOR =  (0x00);
  GPIOD_PDOR |= (1<<3);
  GPIOD_PDDR |= (1<<2);  
  GPIOD_PDDR |= (1<<3);
  
  SPI1_C1 = SPI_C1_MSTR_MASK | SPI_C1_SSOE_MASK;   //Set SPI1 to Master & SS pin to auto SS  
    
  SPI1_C2 = SPI_C2_MODFEN_MASK;   //Master SS pin acts as slave select output   
    
  SPI1_BR = (SPI_BR_SPPR(0x01) | SPI_BR_SPR(0x00));     //Set baud rate prescale divisor to 0 & set baud rate divisor to 2 for baud rate of 16Mhz  
    
  SPI1_C1 |= SPI_C1_SPE_MASK;    //Enable SPI1  
  
}  
  
void spi_send_data(char spiMsg)    //rs = 1
{    
  volatile int i;  
  FGPIOD_PCOR |= (1 << 4);  //Set SS low  //Set CS low  
  FGPIOD_PSOR |= (1 << 2);  //Set RS high / A0 high  
  
  while(!(SPI_S_SPTEF_MASK & SPI1_S));   
        
  SPI1_D = spiMsg;     //Write char to SPI     
  
  FGPIOD_PSOR |= (1 << 4);   //Set CS high  
}   

void spi_send_command(char spiMsg)    //rs = 0
{    
  volatile int i;  
  FGPIOD_PCOR |= (1 << 4);  //Set CS low  
  FGPIOD_PCOR |= (1 << 2);  //Set RS low / A0 low 
    
  while(!(SPI_S_SPTEF_MASK & SPI1_S));    
  
  SPI1_D = spiMsg;     //Write char to SPI     
  
  FGPIOD_PSOR |= (1 << 4);   //Set CS high  
}   

